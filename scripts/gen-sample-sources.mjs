#!/usr/bin/env node
// Generates Codebase/Frontend/src/components/analysis/sampleSources.generated.ts
//
// Why: SamplePickerModal originally used Vite's `import.meta.glob(...)` to bundle every
// C++ sample under Codebase/Microservice/samples as raw text. That is a Vite-only feature
// and breaks under Next.js/webpack (B2 migration, D89). This script emits an explicit
// `?raw` import map instead — `?raw` works in BOTH Vite (native) and Next (the
// asset/source webpack rule in FrontendNext/next.config.js), so the same source compiles
// on either bundler. The emitted keys match the old glob key format
// (`../../../../Microservice/samples/<dir>/<file>.cpp`) so SamplePickerModal's
// buildSamples() regex keeps working unchanged.
//
// Re-run whenever samples are added/removed:  node scripts/gen-sample-sources.mjs
import { readdirSync, statSync, writeFileSync } from 'node:fs';
import { join, relative, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SAMPLES_DIR = join(ROOT, 'Codebase', 'Microservice', 'samples');
const OUT_FILE = join(
  ROOT,
  'Codebase', 'Frontend', 'src', 'components', 'analysis', 'sampleSources.generated.ts',
);
// SamplePickerModal lives in Codebase/Frontend/src/components/analysis/, so the import
// specifier prefix to reach Codebase/Microservice/samples is four levels up.
const SPECIFIER_PREFIX = '../../../../Microservice/samples/';

function walkCpp(dir) {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) out.push(...walkCpp(full));
    else if (entry.endsWith('.cpp')) out.push(full);
  }
  return out;
}

const files = walkCpp(SAMPLES_DIR)
  .map((f) => relative(SAMPLES_DIR, f).split('\\').join('/'))
  .sort();

const importLines = files.map((rel, i) => `import s${i} from '${SPECIFIER_PREFIX}${rel}?raw';`);
const mapLines = files.map((rel, i) => `  '${SPECIFIER_PREFIX}${rel}': s${i},`);

const banner = `// AUTO-GENERATED by scripts/gen-sample-sources.mjs — DO NOT EDIT BY HAND.
// Explicit ?raw imports of every Codebase/Microservice/samples/**/*.cpp, replacing the
// Vite-only import.meta.glob so the same source builds under Next.js/webpack too (D89).
// Re-run: node scripts/gen-sample-sources.mjs
`;

const body = `${banner}
${importLines.join('\n')}

// Keyed by the original glob path so SamplePickerModal.buildSamples() is unchanged.
export const RAW_SAMPLES: Record<string, string> = {
${mapLines.join('\n')}
};
`;

writeFileSync(OUT_FILE, body, 'utf8');
console.log(`[gen-sample-sources] wrote ${files.length} samples -> ${relative(ROOT, OUT_FILE)}`);
